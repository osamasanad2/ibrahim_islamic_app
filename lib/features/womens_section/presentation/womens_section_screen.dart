class WomensSectionScreen extends ConsumerStatefulWidget {
  const WomensSectionScreen({super.key});

  @override
  ConsumerState<WomensSectionScreen> createState() => _WomensSectionScreenState();
}

class _WomensSectionScreenState extends ConsumerState<WomensSectionScreen> {
  int? _expandedSection;

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: AppColors.navy,
      appBar: AppBar(
        title: const Text('قسم المرأة'),
        backgroundColor: AppColors.navy,
        elevation: 0,
        centerTitle: true,
        titleTextStyle: const TextStyle(
          color: AppColors.gold,
          fontFamily: 'Amiri',
          fontSize: 22,
          fontWeight: FontWeight.w700,
        ),
        leading: IconButton(
          icon: const Icon(Icons.arrow_back_rounded, color: AppColors.gold),
          onPressed: () => Navigator.of(context).pop(),
        ),
      ),
      body: ListView.builder(
        padding: const EdgeInsets.fromLTRB(
          AppDimensions.lg,
          AppDimensions.sm,
          AppDimensions.lg,
          AppDimensions.xxl,
        ),
        itemCount: _sections.length,
        itemBuilder: (context, index) {
          final section = _sections[index];
          final isExpanded = _expandedSection == index;
          return _buildSectionCard(section, isExpanded, index);
        },
      ),
    );
  }

  Widget _buildSectionCard(_WomensSection section, bool isExpanded, int index) {
    return Container(
      margin: const EdgeInsets.only(bottom: AppDimensions.md),
      decoration: BoxDecoration(
        color: AppColors.navyLight,
        borderRadius: BorderRadius.circular(AppDimensions.radiusLg),
        border: Border.all(
          color: isExpanded ? AppColors.gold : AppColors.goldMuted,
          width: isExpanded ? 1.5 : 1.0,
        ),
      ),
      child: Column(
        children: [
          GestureDetector(
            onTap: () {
              setState(() {
                _expandedSection = isExpanded ? null : index;
              });
            },
            child: Padding(
              padding: const EdgeInsets.all(AppDimensions.lg),
              child: Row(
                children: [
                  Text(section.icon, style: const TextStyle(fontSize: 26)),
                  const SizedBox(width: AppDimensions.md),
                  Expanded(
                    child: Text(
                      section.title,
                      style: const TextStyle(
                        color: AppColors.gold,
                        fontFamily: 'Amiri',
                        fontSize: 18,
                        fontWeight: FontWeight.w700,
                      ),
                    ),
                  ),
                  Badge(
                    label: Text(
                      '${section.items.length}',
                      style: const TextStyle(
                        fontSize: 11,
                        color: AppColors.navy,
                        fontFamily: 'Inter',
                        fontWeight: FontWeight.w700,
                      ),
                    ),
                    backgroundColor: AppColors.gold,
                    textColor: AppColors.navy,
                  ),
                  const SizedBox(width: AppDimensions.sm),
                  Icon(
                    isExpanded
                        ? Icons.keyboard_arrow_up_rounded
                        : Icons.keyboard_arrow_down_rounded,
                    color: AppColors.gold,
                    size: 28,
                  ),
                ],
              ),
            ),
          ),
          AnimatedCrossFade(
            firstChild: const SizedBox.shrink(),
            secondChild: _buildSectionContent(section),
            crossFadeState:
                isExpanded ? CrossFadeState.showSecond : CrossFadeState.showFirst,
            duration: const Duration(milliseconds: 300),
          ),
        ],
      ),
    );
  }

  Widget _buildSectionContent(_WomensSection section) {
    return ListView.separated(
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      padding: const EdgeInsets.fromLTRB(
        AppDimensions.lg,
        0,
        AppDimensions.lg,
        AppDimensions.lg,
      ),
      itemCount: section.items.length,
      separatorBuilder: (_, __) => const SizedBox(height: AppDimensions.sm),
      itemBuilder: (context, i) {
        final item = section.items[i];
        return _buildSubSection(item);
      },
    );
  }

  Widget _buildSubSection(_SectionItem item) {
    return Container(
      padding: const EdgeInsets.all(AppDimensions.md),
      decoration: BoxDecoration(
        color: AppColors.navy,
        borderRadius: BorderRadius.circular(AppDimensions.radiusMd),
        border: Border.all(
          color: AppColors.goldMuted.withValues(alpha: 0.3),
        ),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Container(
                margin: const EdgeInsets.only(top: 3),
                width: 8,
                height: 8,
                decoration: const BoxDecoration(
                  shape: BoxShape.circle,
                  color: AppColors.gold,
                ),
              ),
              const SizedBox(width: AppDimensions.sm),
              Expanded(
                child: Text(
                  item.title,
                  style: const TextStyle(
                    color: AppColors.goldLight,
                    fontFamily: 'Amiri',
                    fontSize: 16,
                    fontWeight: FontWeight.w700,
                    height: 1.4,
                  ),
                ),
              ),
            ],
          ),
          const SizedBox(height: AppDimensions.sm),
          Text(
            item.content,
            textDirection: TextDirection.rtl,
            style: const TextStyle(
              color: AppColors.textOnDarkMuted,
              fontFamily: 'Amiri',
              fontSize: 14,
              height: 1.8,
            ),
          ),
        ],
      ),
    );
  }
}
